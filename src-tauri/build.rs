fn ensure_windows_icon() {
    let icon_directory = Path::new("icons");
    let icon_path = icon_directory.join("icon.ico");

    if icon_path.exists() {
        return;
    }

    fs::create_dir_all(icon_directory)
        .expect("failed to create the Tauri icons directory");
    fs::write(&icon_path, FALLBACK_ICON)
        .expect("failed to write the fallback Windows icon");

    println!("cargo:warning=Generated fallback Tauri icon at {}", icon_path.display());
}

fn main() {
    ensure_windows_icon();
    tauri_build::build();
}
